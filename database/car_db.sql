-- 1. 지역 테이블
CREATE TABLE region (
    region_id INT AUTO_INCREMENT PRIMARY KEY,
    region_name VARCHAR(50) NOT NULL UNIQUE
);

-- 2. 차종 테이블
CREATE TABLE vehicle_type (
    type_id INT AUTO_INCREMENT PRIMARY KEY,
    type_name VARCHAR(20) NOT NULL UNIQUE
);

-- 3. 용도 테이블
CREATE TABLE usage_type (
    usage_id INT AUTO_INCREMENT PRIMARY KEY,
    usage_name VARCHAR(20) NOT NULL UNIQUE
);

-- 4. 차량 통계 테이블 (핵심)
CREATE TABLE vehicle_stat (
    stat_id INT AUTO_INCREMENT PRIMARY KEY,
    stat_year_month CHAR(6) NOT NULL,  -- 예: 202602
    region_id INT,
    type_id INT,
    usage_id INT,
    vehicle_count INT NOT NULL,

    FOREIGN KEY (region_id) REFERENCES region(region_id),
    FOREIGN KEY (type_id) REFERENCES vehicle_type(type_id),
    FOREIGN KEY (usage_id) REFERENCES usage_type(usage_id)
);

