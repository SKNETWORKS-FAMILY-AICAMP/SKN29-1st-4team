SELECT 
    v.stat_year_month,
    r.region_name,
    t.type_name,
    u.usage_name,
    v.vehicle_count
FROM vehicle_stat v
JOIN region r ON v.region_id = r.region_id
JOIN vehicle_type t ON v.type_id = t.type_id
JOIN usage_type u ON v.usage_id = u.usage_id
ORDER BY r.region_name, t.type_name, u.usage_name;

